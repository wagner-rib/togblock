export function isSupported(ch: string): boolean {
  return Object.prototype.hasOwnProperty.call(FONT, ch);
}

export function countFilledCells(ch: string): number {
  const rows = FONT[ch];
  if (!rows) return 0;
  return rows.reduce((n, row) => n + [...row].filter((c) => c === '1').length, 0);
}
